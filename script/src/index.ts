let add_white_list = async (vec_address: string[]) => {
  if (vec_address.length > 500) {
    console.log("too much address to check");
    return;
  }

  const connection = new Connection({
    fullnode: "https://fullnode.testnet.sui.io:443",
    faucet: "https://faucet.devnet.sui.io/gas",
  });

  const keypair = Ed25519Keypair.deriveKeypair(
    "surround park unknown crane regular episode liquid file increase rescue warfare west"
  );

  const pkg =
    "0xa4ad1545666eb4cd3d0d284711a2598f46afb5afc801c0209c805f701952e4be";
  const pool =
    "0xf802a1db3ff541005d8df0699aeccd7e1da281baa74fd1c4ac774ccdaa922b3f";
  const admincap =
    "0x97036c6bc5576c0ab0955a94fd85109c7053dfd63d2655b885078ba5e00439bf";

  const provider = new JsonRpcProvider(connection);
  const signer = new RawSigner(keypair, provider);

  const txBlock = new TransactionBlock();
  txBlock.setGasBudget(1000000000);
  const max_pure_argument_size = 16 * 1024;
  const vec_address_bytes = bcs
    .ser("vector<address>", vec_address, { maxSize: max_pure_argument_size })
    .toBytes();

  txBlock.moveCall({
    target: `${pkg}::ido::add_white_list`,
    arguments: [
      txBlock.object(pool),
      txBlock.object(admincap),
      txBlock.pure(vec_address_bytes),
    ],
    typeArguments: ["0x2::sui::SUI"],
  });

  const result = await signer.signAndExecuteTransactionBlock({
    transactionBlock: txBlock,
    options: {
      showEffects: true,
      showObjectChanges: true,
    },
  });

  console.log({ result });
};

